package com.indusvalley.oauth.model.json.callback;

public class FBGraphAPILocation {

	private String id;
	private String name;

	public FBGraphAPILocation() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
